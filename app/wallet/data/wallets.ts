// Helper functions
export function getWalletsByType(type: WalletType): Wallet[] {
  return wallets.filter((w) => w.type === type)
}

export function getWalletsWithClassicOS(): Wallet[] {
  return wallets.filter((w) => w.supportsClassicOS)
}

export function getWalletsBySecurityLevel(level: 'high' | 'medium' | 'standard'): Wallet[] {
  return wallets.filter((w) => w.securityLevel === level)
}

export function getWalletByName(name: string): Wallet | undefined {
  return wallets.find((w) => w.name.toLowerCase() === name.toLowerCase())
}
